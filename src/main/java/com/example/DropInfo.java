package com.example;

public class DropInfo {
    private String npcName;
    private String npcSubmissionName;
    private String dropName;
    private String dropSubmissionName;

    public DropInfo(String npcName, String npcSubmissionName, String dropName, String dropSubmissionName) {
        this.npcName = npcName;
        this.npcSubmissionName = npcSubmissionName;
        this.dropName = dropName;
        this.dropSubmissionName = dropSubmissionName;
    }


}
